void main() {
  print("Operaciones Aritméticas");
  final double costoVacuna = 12.5;
  final double costoAlimento = 5;
  print("Suma  $costoVacuna + $costoAlimento = ${costoVacuna + costoAlimento}");
  print("Resta  $costoVacuna - $costoAlimento = ${costoVacuna - costoAlimento}");
  print("Multiplicación  $costoVacuna * $costoAlimento = ${costoVacuna * costoAlimento}");
  print("División  $costoVacuna / $costoAlimento = ${costoVacuna / costoAlimento}");
}
